let locale = SUPPORTED.has(localStorage.getItem(STORAGE_KEY)) ? localStorage.getItem(STORAGE_KEY) : "zh-TW";

export function getLocale() { return locale; }
export function isEnglish() { return locale === "en"; }
export function t(text) { return isEnglish() ? (en[text] || text) : text; }

function translateTextNode(textNode) {
  if (!isEnglish() || !textNode.nodeValue?.trim()) return;
  const leading = textNode.nodeValue.match(/^\s*/)?.[0] || "";
  const trailing = textNode.nodeValue.match(/\s*$/)?.[0] || "";
  const source = textNode.nodeValue.trim();
  if (en[source]) textNode.nodeValue = `${leading}${en[source]}${trailing}`;
}

function translateTree(root = document.body) {
  if (!isEnglish()) return;
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach(translateTextNode);
  for (const element of root.querySelectorAll("[placeholder], [title], [aria-label]")) {
    for (const attribute of ["placeholder", "title", "aria-label"]) {
      const value = element.getAttribute(attribute);
      if (value && en[value]) element.setAttribute(attribute, en[value]);
    }
  }
}

export function applyLocale() {
  document.documentElement.lang = locale;
  document.documentElement.dataset.locale = locale;
  const selector = document.getElementById("languageSelect");
  if (selector) selector.value = locale;
  translateTree();
}

export function initI18n() {
  applyLocale();
  document.getElementById("languageSelect")?.addEventListener("change", event => {
    const next = event.target.value;
    if (!SUPPORTED.has(next) || next === locale) return;
    localStorage.setItem(STORAGE_KEY, next);
    window.location.reload();
  });
  const observer = new MutationObserver(records => {
    if (!isEnglish()) return;
    for (const record of records) {
      if (record.type === "characterData") translateTextNode(record.target);
      else for (const added of record.addedNodes) {
        if (added.nodeType === Node.TEXT_NODE) translateTextNode(added);
        else if (added.nodeType === Node.ELEMENT_NODE) translateTree(added);
      }
    }
  });
  observer.observe(document.body, { childList: true, subtree: true, characterData: true });
}
